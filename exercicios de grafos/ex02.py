grafo = {}
arestas = []
vertices = []

grafo [1] = 5,7
grafo [4] = 1
grafo [3] = 2


for keys in grafo:
    arestas.append(grafo[keys])
    vertices.append(keys)


print(f"Arestas: {arestas}")
print(f"Vertices: {vertices}")

